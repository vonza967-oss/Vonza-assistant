  function normalizeLanguage(value) {
    const normalized = String(value || "").trim().toLowerCase();
    return SUPPORTED_LANGUAGE_CODES.has(normalized) ? normalized : "en";
  }

  function getCachedLanguage() {
    try {
      return normalizeLanguage(global.localStorage?.getItem(STORAGE_KEY));
    } catch {
      return "en";
    }
  }

  function t(key, params = {}, language = getCachedLanguage()) {
    const normalizedLanguage = normalizeLanguage(language);
    const template = translations[normalizedLanguage]?.[key] ?? translations.en[key] ?? key;

    return String(template).replace(/\{(\w+)\}/g, (_match, name) => (
      Object.prototype.hasOwnProperty.call(params, name) ? String(params[name]) : `{${name}}`
    ));
  }

  global.VonzaDashboardI18n = Object.freeze({
    STORAGE_KEY,
    SUPPORTED_LANGUAGES,
    normalizeLanguage,
    getCachedLanguage,
    t,
  });
